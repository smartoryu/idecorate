import React, { useState } from "react";
import { Carousel, CarouselItem, CarouselControl, CarouselIndicators } from "reactstrap";
import { useSelector } from "react-redux";
import { API_URL } from "../../support/API_URL";

export const Slideshow = props => {
  const ImagesSlideshow = useSelector(({ Homepage }) => Homepage.imagesSlideshow);
  const [activeIndex, setActiveIndex] = useState(0);
  const [animating, setAnimating] = useState(false);

  const next = () => {
    if (animating) return;
    const nextIndex = activeIndex === ImagesSlideshow.length - 1 ? 0 : activeIndex + 1;
    setActiveIndex(nextIndex);
  };

  const previous = () => {
    if (animating) return;
    const nextIndex = activeIndex === 0 ? ImagesSlideshow.length - 1 : activeIndex - 1;
    setActiveIndex(nextIndex);
  };

  const goToIndex = newIndex => {
    if (animating) return;
    setActiveIndex(newIndex);
  };

  const slides = ImagesSlideshow.map(image => {
    return (
      <CarouselItem
        className="custom-style"
        tag="div"
        onExiting={() => setAnimating(true)}
        onExited={() => setAnimating(false)}
        key={image.src}>
        <img className="d-block w-100" src={`${API_URL + image.src}`} alt="product" />
      </CarouselItem>
    );
  });

  return (
    <div className="carousel-homepage">
      <style>
        {`.custom-style {
          background-size: cover;
          height: 400px;
        }`}
      </style>
      <Carousel interval={3000} activeIndex={activeIndex} next={next} previous={previous}>
        <CarouselIndicators items={ImagesSlideshow} activeIndex={activeIndex} onClickHandler={goToIndex} />
        {slides}
        <CarouselControl direction="prev" directionText="Previous" onClickHandler={previous} />
        <CarouselControl direction="next" directionText="Next" onClickHandler={next} />
      </Carousel>
    </div>
  );
};

